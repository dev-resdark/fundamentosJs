/**
 * Condicionales - Comparación == y Operador estricto ===
 * 
 * ambos son para comparar si es igual, el segundo es más estricto que el primero
 */

const num1 = 20
const num2 = '20'

if (num1 == num2 ){
    console.log('son iguales')
} else {
    console.log(' no son iguales')
}
if (num1 === num2 ){
    console.log('son iguales')
} else {
    console.log(' no son iguales')
}
